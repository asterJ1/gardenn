
public class Calculator {
	void powerOn() {
		System.out.println("turn on the power");
	}
	
	int plus(int x, int y) {
		int result = x + y;
		return result;
	}
	
	double divide(int x, int y) {
		double result = (double)x / (double)y;
		return result;
	}
	
	void powerOff() {
		System.out.println("turn off the power");
	}
}
