#시작하기 전에
print(True)
print(False)

#불 만들기: 비교 연산자
print(10 == 100)
print(10 != 100)
print(10 < 100)
print(10 <= 100)
print(10 >= 100)

print("가방" == "가방")
print("가방" != "하마")
print("가방" < "하마") #사전 순서
print("가방" > "하마") #사전 순서

#불 연산하기: 논리 연산자
print(not True)
print(not False)

#and 연산자와 or 연산자
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print(True or True)
print(True or False)
print(False or True)
print(False or False)

#if 조건문이란?
if True:
    print("it is True")
    print("literally it is")

if False:
    print("it is False")